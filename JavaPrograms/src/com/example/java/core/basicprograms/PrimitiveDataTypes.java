package com.example.java.core.basicprograms;

public class PrimitiveDataTypes {

	public static void main(String[] args) {
		
		// Data Types -
		
		// Numeric
		// byte - Integer (Integral Value)
		// short - Integer (Integral Value)
		// int - Integer (Integral Value)
		// long - Integer (Integral Value)
		// float - Floating point (Integral Value)
		// double - Floating point (Integral Value)
		
		// boolean - Boolean Type
		// char - Character Value
		
		byte b = 0;
		System.out.println(b);
		
		short s = 2;
		System.out.println(s);
		
		int i = 12;
		System.out.println(i);
		
		long l = 12;
		System.out.println(l);
		
		float f = 12f;
		System.out.println(f);
		
		double d = 12;
		System.out.println(d);
		
		boolean bool = true;
		System.out.println(bool);
		
		char c = 'a';
		char ch = 67;
		System.out.println(c+" "+ch);
	}

}
