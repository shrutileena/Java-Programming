package com.example.polymorphism;

public class Polymorphism {

	public static void main(String[] args) {
		// objects have different behaviour in different situations
		
		// compile-time - object behaviour is defined at compile time
		// also called early binding, overloading
		// at compile time itself its defined which method will be called depending on parameters
		
		// run-time - object behaviour is defined at run time
		// also called late binding, overriding
		// at run time it is decided which method of which class (parent or child) will get executed
		
	}

}
