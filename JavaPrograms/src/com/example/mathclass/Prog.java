package com.example.mathclass;

public class Prog {

	public static void main(String[] args) {
		
		System.out.println(Math.abs(-737));
		
		System.out.println(Math.pow(2, 3));
		
		System.out.println(Math.absExact(-6564));
		
		System.out.println(Math.ceil(46.7));
		
		System.out.println(Math.floor(46.3));
		
		System.out.println(Math.incrementExact(2));
		
		System.out.println(Math.round(2.5));
		
		System.out.println(Math.round(2.2));
		
		System.out.println(Math.min(1, 4638));
		
		System.out.println(Math.min(3.4, 2.4));
		
		System.out.println(Math.max(5.6, 10.3));
		
		System.out.println(Math.sqrt(4));
		
		System.out.println(Math.PI);
		
		System.out.println(Math.E);
		
		System.out.println(Math.addExact(2, 2));
		
		System.out.println(Math.addExact(6524523636L, 286458935L));
		
		System.out.println(Math.exp(3));
	}

}
